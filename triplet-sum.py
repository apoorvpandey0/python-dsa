    def threeSum(self, nums):
        """
            Bas kaam karta hai types solution.
            Time: O(n^2)
        """
        # print(nums)
        if len(nums)<3:
            return []
        checked = set()
        triplets = set()
        for i in range(len(nums)):
            # print(i)
            if nums[i] in checked:
                continue
            arr = copy.deepcopy(nums)
            arr.pop(i)
            # print(arr)
    
            target = 0 - nums[i]
            myset = set()
            for j in range(len(arr)):
                temp = target - arr[j]
                # print("Pair target ",temp)
                if temp in myset:
                    if len(list([nums[i],arr[j],temp]))==3:
                        # print(nums[i],arr[j],temp)
                        triplets.add((tuple(sorted([nums[i],arr[j],temp]))))
                myset.add(arr[j])
            checked.add(nums[i])
        return [(list(i)) for i in triplets]
            
                
